window.NIRAVAN_CURRENT_LANG = localStorage.getItem("NIRAVAN_LANG") || "en";

window.translateElement = function(element) {
  if (!element) return;
  const key = element.getAttribute("data-i18n");
  if (!key) return;
  
  const translations = window.NIRAVAN_I18N[window.NIRAVAN_CURRENT_LANG];
  if (translations && translations[key]) {
    if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
      element.setAttribute("placeholder", translations[key]);
    } else {
      element.textContent = translations[key];
    }
  }
};

window.setLanguage = function(lang) {
  if (lang !== "en" && lang !== "ta") return;
  window.NIRAVAN_CURRENT_LANG = lang;
  localStorage.setItem("NIRAVAN_LANG", lang);
  
  // Update all data-i18n elements
  document.querySelectorAll("[data-i18n]").forEach(el => {
    window.translateElement(el);
  });
  
  // Update HTML lang attribute
  document.documentElement.setAttribute("lang", lang);
  
  // Broadcast language change event
  const event = new CustomEvent("niravanLanguageChanged", { detail: lang });
  window.dispatchEvent(event);
  
  console.log(`[NIRAVAN] Language switched to: ${lang}`);
};

// Initial page translation on DOM load
document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    window.translateElement(el);
  });
  document.documentElement.setAttribute("lang", window.NIRAVAN_CURRENT_LANG);
});
